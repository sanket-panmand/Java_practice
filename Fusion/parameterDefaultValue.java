package Fusion;
						// Default value for all parameter
public class parameterDefaultValue {

	boolean a;
	Short b;
	char c;
	int d;
	long e;
	float f;
	double g;
	
	
	public static void main(String[] args) {
				
		parameterDefaultValue t1 =new parameterDefaultValue();
		System.out.println("boolean : " + (t1.a));
		System.out.println("short : " + (t1.b));
		System.out.println("char: " + (t1.c));
		System.out.println("int : " + (t1.d));
		System.out.println("long : " + (t1.e));
		System.out.println("Float : " + (t1.f));
		System.out.println("Double : " + (t1.g));
	}

}
